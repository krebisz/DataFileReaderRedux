namespace DataFileReader.Class
{
    public class StandardJSON
    {
        private string json = string.Empty;

        public StandardJSON()
        { }

        public StandardJSON(string jsonInput)
        {
            json = StandardizeJSON(jsonInput);
        }

        public static string StandardizeJSON(string jsonInput)
        {
            string formattedJSON = jsonInput;

            //-------------------------------------------------//-------------------------------------------------
            //-------------------------------------------------//-------------------------------------------------
            //                                  POTENTIAL FORMATS: (AXIOMS - To Handle)
            //-------------------------------------------------//-------------------------------------------------
            //-------------------------------------------------//-------------------------------------------------
            //
            //
            //
            //-------------------------------------------------//-------------------------------------------------
            //0 ORDER:
            //-------------------------------------------------//-------------------------------------------------
            // ∅ - (representation of null or empty)
            //
            //
            //
            //-------------------------------------------------//-------------------------------------------------
            //1ST ORDER:
            //-------------------------------------------------//-------------------------------------------------
            // {}  - Handle as:
            // []  - Handle as:
            // {*} - Handle as, then handover to:
            // [*] - Handle as, then handover to:
            //
            //
            //
            //-------------------------------------------------//-------------------------------------------------
            //2ND ORDER:
            //-------------------------------------------------//-------------------------------------------------
            //{{}}
            //{[]}
            //[{}]
            //[[]]

            return formattedJSON;
        }
    }
}